"""
E2_1 finite difference stencils for SHOCCS.

Translation of E2_1.cpp - a 1st order accurate E2-type stencil.
This stencil uses 4 parameters (alpha[0-3]) and provides derivatives
with 1st order accuracy (P=1), 4 rows in near-boundary stencils (R=4),
and tail length 5 (T=5).

Constants:
    P: Order of accuracy (1)
    R: Number of rows in near-boundary stencil (4)
    T: Tail length for boundary stencils (5)
    X: Extra boundary parameter (0)

The stencil uses one parameter array:
    - alpha: Four stencil parameters (4 elements)
"""

import numpy as np
from numba import njit


# Stencil constants
P = 1  # Order of accuracy
R = 4  # Number of rows
T = 5  # Tail length
X = 0  # Extra parameter


class E2_1Stencil:
    """
    E2_1 stencil configuration.

    Simple data container for stencil parameters. Not meant to be
    used in performance-critical code (use the @njit functions instead).

    Attributes:
        alpha: Stencil parameters (4 elements)
    """

    def __init__(self, alpha: np.ndarray = None):
        """
        Initialize E2_1 stencil parameters.

        Args:
            alpha: Stencil parameters (will be padded/truncated to 4)
        """
        # Initialize with zeros and copy provided values
        self.alpha = np.zeros(4, dtype=np.float64)

        if alpha is not None:
            n = min(len(alpha), 4)
            self.alpha[:n] = alpha[:n]


@njit
def interior(h: float) -> np.ndarray:
    """
    Interior stencil for E2_1 (1st derivative).

    Simple 3-point centered difference: [-1, 0, 1] / (2h)

    Args:
        h: Grid spacing

    Returns:
        Stencil coefficients [c_{-1}, c_0, c_1]
    """
    c = np.zeros(3, dtype=np.float64)
    c[0] = -1.0 / (2.0 * h)
    c[1] = 0.0
    c[2] = 1.0 / (2.0 * h)

    return c


@njit
def interp_interior(y: float) -> np.ndarray:
    """
    Interior interpolation stencil.

    Linear interpolation between two grid points.

    Args:
        y: Interpolation offset (-1 <= y <= 1)

    Returns:
        Interpolation coefficients (2 elements)
    """
    c = np.zeros(2, dtype=np.float64)

    if y > 0.0:
        c[0] = 1.0 + -1.0 * y
        c[1] = y
    else:
        c[0] = -1.0 * y
        c[1] = 1.0 + y

    return c


@njit
def interp_wall(
    i: int,
    y: float,
    psi: float,
    alpha: np.ndarray,
    right: bool
) -> np.ndarray:
    """
    Wall interpolation stencil.

    Interpolation near boundaries using stencil parameters.

    Args:
        i: Stencil index (0 or 1)
        y: Interpolation offset
        psi: Boundary stretching parameter
        alpha: Stencil parameters (4 elements)
        right: True for right boundary, False for left

    Returns:
        Interpolation coefficients (3 elements)
    """
    c = np.zeros(3, dtype=np.float64)

    if right:
        t6 = 1.0 + psi
        t7 = 1.0 / t6
        t8 = -1.0 + y
        t9 = psi * t8
        t5 = -1.0 + psi
        t12 = -1.0 * psi * y
        t20 = 1.0 + y

        if i == 0:
            c[0] = (psi + y + psi * y) * t5
            c[1] = 1.0 + t12 + -1.0 * psi * psi * t20 + y
            c[2] = psi * t20
        elif i == 1:
            c[0] = (t9 + y) * t5 * t7
            c[1] = psi + t12
            c[2] = (1.0 + t9 + y) * t7
    else:
        t8 = -1.0 + y
        t11 = psi * y
        t13 = -1.0 + psi
        t17 = 1.0 + psi
        t18 = 1.0 / t17

        if i == 0:
            c[0] = psi + -1.0 * psi * y
            c[1] = 1.0 + t11 + psi * psi * t8 + -1.0 * y
            c[2] = (psi * t8 + y) * -1.0 * t13
        elif i == 1:
            c[0] = (-1.0 + psi + t11 + y) * -1.0 * t18
            c[1] = (1.0 + y) * psi
            c[2] = (psi + t11 + y) * -1.0 * t13 * t18

    return c



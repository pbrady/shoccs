def nbs(
    h: float,
    bc_type: str,
    psi: float,
    params: E2_1Stencil,
    right: bool
) -> np.ndarray:
    """
    Dispatch function for near-boundary stencils.

    Args:
        h: Grid spacing
        bc_type: Boundary condition type ('floating', 'dirichlet', 'neumann')
        psi: Boundary stretching parameter
        params: E2_1 stencil parameters
        right: True for right boundary, False for left

    Returns:
        Near-boundary stencil coefficients
    """
    bc_type = bc_type.lower()

    if bc_type == 'floating':
        return nbs_floating(h, psi, params.alpha, right)
    elif bc_type == 'dirichlet':
        return nbs_dirichlet(h, psi, params.alpha, right)
    elif bc_type == 'neumann':
        return nbs_neumann(h, psi, params.alpha, right)
    else:
        raise ValueError(f"Unknown boundary condition type: {bc_type}")


